// literals represent a fixed value - Can come in a variety of forms - Something that developers add to code
// literals are hardcoded - Important to note

//string literal

let car ='ford';

//numeric literal

let december = 12;

//boolean literal

let tired = true;

// object literal

let soup = { // This is an object - uses {}
    a: 'chicken noodle',
    b: 'tomato',
    c:'beef and barley',
}

console.log(soup.c) //grabs onto the c key from the object

// ARRAY LITERALS
let days = ['Monday', 'Tuesday', 'Wednesday', 'Thursday', 'Friday']; //this is an array - Uses []

console.log(days.length); //Gives the length of array - Number of objects

console.log(days.toString()); // Converts the array to a string

